<H1>Welcome to Cerberus Helpdesk!</H1>

<p>Cerberus Helpdesk 4.0 is a team-based, web-mail application built for organizations of all sizes.</p>

<h2>What is web-mail?</h2>

<p>A web-mail application is a browser-based e-mail reader.  The major advantage of web-mail 
is you can have access to your e-mail inbox and history from any computer with an Internet 
connection.</p>

<h2>What is a ticket?</h2>

<p>A ticket is the name for a helpdesk conversation and all the data you associate with it.  
Each ticket is given a short, unique ID which is used to refer to a specific conversation.</p>

<h2>What is a team?</h2>

<p>Teams are simply groups of people sharing web-mail for a common purpose. Many 
organizations find it helpful to start with a couple department or project-related teams (e.g., Support, 
Sales, Development, Marketing) which are later broken down into smaller groups as needed (by project, location, 
technical wizardry, etc.)</p>
